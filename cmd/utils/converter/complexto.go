package converter

// Complex64ToInt는 64비트 복소수를 정수(int)로 변환합니다.
func Complex64ToInt(c complex64) int {
	return int(real(c))
}

// Complex64ToUint는 64비트 복소수를 부호 없는 정수(uint)로 변환합니다.
func Complex64ToUint(c complex64) uint {
	return uint(real(c))
}

